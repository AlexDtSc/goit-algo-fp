def dynamic_programming(items, budget):
    item_names = list(items.keys())
    n = len(item_names)

    # –°—Ç–≤–æ—Ä–µ–Ω–Ω—è DP-—Ç–∞–±–ª–∏—Ü—ñ: dp[i][b] ‚Äî –º–∞–∫—Å–∏–º—É–º –∫–∞–ª–æ—Ä—ñ–π, —è–∫—ñ –º–æ–∂–Ω–∞ –æ—Ç—Ä–∏–º–∞—Ç–∏ –∑ –ø–µ—Ä—à–∏—Ö i –ø—Ä–µ–¥–º–µ—Ç—ñ–≤ –ø—Ä–∏ –±—é–¥–∂–µ—Ç—ñ b
    dp = [[0] * (budget + 1) for _ in range(n + 1)]

    # –ó–∞–ø–æ–≤–Ω–µ–Ω–Ω—è —Ç–∞–±–ª–∏—Ü—ñ
    for i in range(1, n + 1):
        name = item_names[i - 1]
        cost = items[name]["cost"]
        cal = items[name]["calories"]

        for b in range(budget + 1):
            if cost <= b:
                # –º–∞–∫—Å–∏–º—É–º –∑ (—á–∏ –≤–∑—è—Ç–∏ —Å—Ç—Ä–∞–≤—É, —á–∏ –Ω–µ –≤–∑—è—Ç–∏ —Å—Ç—Ä–∞–≤—É)
                dp[i][b] = max(dp[i - 1][b - cost] + cal,  
                               dp[i - 1][b])
            else:
                # –Ω–µ –º–æ–∂–µ–º–æ –≤–∑—è—Ç–∏ —Å—Ç—Ä–∞–≤—É
                dp[i][b] = dp[i - 1][b]

    # –í—ñ–¥–Ω–æ–≤–ª–µ–Ω–Ω—è –≤–∏–±—Ä–∞–Ω–∏—Ö –ø—Ä–µ–¥–º–µ—Ç—ñ–≤
    chosen_items = []
    b = budget
    for i in range(n, 0, -1):
        if dp[i][b] != dp[i - 1][b]:
            name = item_names[i - 1]
            chosen_items.append(name)
            b -= items[name]["cost"]

    total_calories = dp[n][budget]
    total_cost = budget - b
    chosen_items.reverse()  # –©–æ–± –≤–∏–≤–æ–¥–∏—Ç–∏ –≤ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º—É –ø–æ—Ä—è–¥–∫—É

    return total_calories, total_cost, chosen_items


