def merge_sorted_lists(list1: LinkedList, list2: LinkedList) -> LinkedList:
    dummy = Node()  # 1. –ü—É—Å—Ç–∏–π –≤—É–∑–æ–ª-¬´–∑–∞–≥–ª—É—à–∫–∞¬ª –¥–ª—è –∑—Ä—É—á–Ω–æ—Å—Ç—ñ
