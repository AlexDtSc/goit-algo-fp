import turtle
import math

def draw_tree(t, length, depth):
    if depth == 0:                # –Ø–∫—â–æ –≥–ª–∏–±–∏–Ω–∞ –¥–æ—Ä—ñ–≤–Ω—é—î –Ω—É–ª—é, –∑—É–ø–∏–Ω—è—î–º–æ –º–∞–ª—é–≤–∞–Ω–Ω—è ‚Äî —Ü–µ –∫—ñ–Ω—Ü–µ–≤–∞ —Ç–æ—á–∫–∞ –¥–µ—Ä–µ–≤–∞.
        return

    # –ú–∞–ª—é—î–º–æ –≥—ñ–ª–∫—É
    t.forward(length)             # –ß–µ—Ä–µ–ø–∞—à–∫–∞ —Ä—É—Ö–∞—î—Ç—å—Å—è –≤–ø–µ—Ä–µ–¥ –Ω–∞ length –ø—ñ–∫—Å–µ–ª—ñ–≤ ‚Äî —Ü–µ –æ–¥–Ω–∞ –≥—ñ–ª–∫–∞ –¥–µ—Ä–µ–≤–∞.
