class Node:
  def __init__(self, data=None):
    self.data = data
    self.next = None


class LinkedList:
  def __init__(self):
    self.head = None

  def insert_at_beginning(self, data):
    new_node = Node(data)
    new_node.next = self.head
    self.head = new_node

  def insert_at_end(self, data):
    new_node = Node(data)
    if self.head is None:
      self.head = new_node
    else:
      cur = self.head
      while cur.next:
        cur = cur.next
      cur.next = new_node

  def insert_after(self, prev_node: Node, data):
    if prev_node is None:
      print("–ü–æ–ø–µ—Ä–µ–¥–Ω—å–æ–≥–æ –≤—É–∑–ª–∞ –Ω–µ —ñ—Å–Ω—É—î.")
      return
    new_node = Node(data)
    new_node.next = prev_node.next
    prev_node.next = new_node

  def insert_before(self, next_node_data, data):
        if self.head is None:
            print("–°–ø–∏—Å–æ–∫ –ø–æ—Ä–æ–∂–Ω—ñ–π.")
            return
        # –Ø–∫—â–æ –≤—Å—Ç–∞–≤–∫–∞ –ø–µ—Ä–µ–¥ –≥–æ–ª–æ–≤–æ—é
        if self.head.data == next_node_data:
            new_node = Node(data)
            new_node.next = self.head
            self.head = new_node
            return
        # –ü–æ—à—É–∫ next_node —É —Å–µ—Ä–µ–¥–∏–Ω—ñ —Å–ø–∏—Å–∫—É
        prev = None
        current = self.head
        while current and current.data != next_node_data:
            prev = current
            current = current.next
        if current is None:
            print("–í—É–∑–æ–ª, –ø–µ—Ä–µ–¥ —è–∫–∏–º —Ç—Ä–µ–±–∞ –≤—Å—Ç–∞–≤–∏—Ç–∏, –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ.")
            return

        # –í—Å—Ç–∞–≤–∫–∞ –Ω–æ–≤–æ–≥–æ –≤—É–∑–ª–∞ –º—ñ–∂ prev —ñ current
        new_node = Node(data)
        new_node.next = current
        prev.next = new_node 
     

  def delete_node(self, key: int):
    cur = self.head
    if cur and cur.data == key:
      self.head = cur.next
      cur = None
      return
    prev = None
    while cur and cur.data != key:
      prev = cur
      cur = cur.next
    if cur is None:
      return
    prev.next = cur.next
    cur = None

  def search_element(self, data: int) -> Node | None:
    cur = self.head
    while cur:
      if cur.data == data:
        return cur
      cur = cur.next
    return None

  def print_list(self):
    current = self.head
    while current:
      print(current.data)
      current = current.next


