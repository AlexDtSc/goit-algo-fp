## –†–µ–∞–ª—ñ–∑–∞—Ü—ñ—è –∞–ª–≥–æ—Ä–∏—Ç–º—É –î–µ–π–∫—Å—Ç—Ä–∏ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è–º –±—ñ–Ω–∞—Ä–Ω–æ—ó –∫—É–ø–∏ (–ø—ñ—Ä–∞–º—ñ–¥–∏)

def dijkstra(graph, start):
    # –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑—É—î–º–æ –Ω–∞–π–∫–æ—Ä–æ—Ç—à—ñ —à–ª—è—Ö–∏: –Ω–µ—Å–∫—ñ–Ω—á–µ–Ω–Ω—ñ—Å—Ç—å –¥–ª—è –≤—Å—ñ—Ö, –∫—Ä—ñ–º —Å—Ç–∞—Ä—Ç–æ–≤–æ—ó –≤–µ—Ä—à–∏–Ω–∏
    shortest_paths = {vertex: float('inf') for vertex in graph}
    shortest_paths[start] = 0

    # –ß–µ—Ä–≥–∞ –∑ –ø—Ä—ñ–æ—Ä–∏—Ç–µ—Ç–æ–º: (–≤–∞–≥–∞, –≤–µ—Ä—à–∏–Ω–∞)
    priority_queue = [(0, start)]

    while priority_queue:
        current_distance, current_vertex = heapq.heappop(priority_queue)

        # –Ø–∫—â–æ –º–∏ –≤–∂–µ –∑–Ω–∞–π—à–ª–∏ –∫–æ—Ä–æ—Ç—à–∏–π —à–ª—è—Ö ‚Äî –ø—Ä–æ–ø—É—Å–∫–∞—î–º–æ
        if current_distance > shortest_paths[current_vertex]:
            continue

        # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —Å—É—Å—ñ–¥—ñ–≤
        for neighbor, weight in graph[current_vertex].items():
            distance = current_distance + weight

            # –Ø–∫—â–æ –∑–Ω–∞–π—à–ª–∏ –∫–æ—Ä–æ—Ç—à–∏–π —à–ª—è—Ö
            if distance < shortest_paths[neighbor]:
                shortest_paths[neighbor] = distance
                heapq.heappush(priority_queue, (distance, neighbor))

    return shortest_paths

## –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è –∞–ª–≥–æ—Ä–∏—Ç–º—É –î–µ–π–∫—Å—Ç—Ä–∏ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è–º –±—ñ–Ω–∞—Ä–Ω–æ—ó –∫—É–ø–∏ (–ø—ñ—Ä–∞–º—ñ–¥–∏)

